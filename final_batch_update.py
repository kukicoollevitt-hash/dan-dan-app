# Apply updates
for unit_num, update_data in units_update.items():
    # Find and replace q2_opts for this unit
    # Pattern to find q2_opts section
    pattern = rf"(on_people2_{unit_num}:.*?q2_text: '각 문단의 중심 내용으로 알맞은 것은\?',\s*q2_opts: \[)(.*?)(\],)"
    
    def replace_opts(match):
        return match.group(1) + "\n        " + update_data['new_opts'] + "\n      " + match.group(3)
    
    content = re.sub(pattern, replace_opts, content, flags=re.DOTALL, count=1)
    
    # Find and replace explain for this unit
    explain_pattern = rf"(on_people2_{unit_num}:.*?explain: \{{.*?q2:)(.*?)(,)"
    
    def replace_explain(match):
        return match.group(1) + update_data['new_explain'] + match.group(3)
    
    content = re.sub(explain_pattern, replace_explain, content, flags=re.DOTALL, count=1)
    
    print(f"Unit {unit_num} updated")

# Write back
with open(file_path, 'w', encoding='utf-8') as f:
    f.write(content)

