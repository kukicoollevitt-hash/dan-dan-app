// 패턴: learning-common.js 로드 전에 REMEDIAL_BANK 삽입
const learningCommonPattern = /(<script[^>]*src="[^"]*learning-common\.js[^"]*"[^>]*><\/script>)/;

for (let i = 11; i <= 20; i++) {
  const num = String(i).padStart(2, '0');
  const unitKey = `fit_bio_${num}`;
  const filePath = path.join(__dirname, 'public', 'BRAINUP', 'science', `fit_bio_${num}.html`);

  if (!fs.existsSync(filePath)) {
    console.log(`[SKIP] ${filePath} 파일 없음`);
    continue;
  }

  let content = fs.readFileSync(filePath, 'utf8');
  const bioData = BIO_DATA[unitKey];

  if (!bioData) {
    console.log(`[SKIP] fit_bio_${num} 데이터 없음`);
    continue;
  }

  // 이미 window.REMEDIAL_BANK가 있는지 확인
  if (content.includes('window.REMEDIAL_BANK = {')) {
    console.log(`[SKIP] fit_bio_${num} 이미 window.REMEDIAL_BANK 존재`);
    continue;
  }

  const match = content.match(learningCommonPattern);
  if (!match) {
    console.log(`[SKIP] fit_bio_${num} learning-common.js 패턴 없음`);
    continue;
  }

  // REMEDIAL_BANK 스크립트 생성
  const remedialScript = `<!-- ✅ 보완학습 데이터 (learning-common.js 로드 전에 정의) -->
<script>
window.REMEDIAL_BANK = ${JSON.stringify(bioData.problems, null, 2)};
</script>

${match[0]}`;

  content = content.replace(learningCommonPattern, remedialScript);
  fs.writeFileSync(filePath, content, 'utf8');
  console.log(`[OK] fit_bio_${num} REMEDIAL_BANK 추가 완료 (${bioData.title})`);
}

console.log('\n✅ fit_bio_11~20 보완학습 데이터 추가 완료!');
