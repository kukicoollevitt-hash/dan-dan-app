// people1_21~30 보완학습 데이터 삽입
for (let i = 21; i <= 30; i++) {
  const num = String(i).padStart(2, '0');
  const filePath = path.join(__dirname, 'public', 'BRAINUP', 'person', `people1_${num}.html`);

  if (!fs.existsSync(filePath)) {
    console.log(`[SKIP] ${filePath} 파일 없음`);
    continue;
  }

  let content = fs.readFileSync(filePath, 'utf8');

  // 이미 REMEDIAL_BANK가 있는지 확인
  if (content.includes('window.REMEDIAL_BANK')) {
    console.log(`[SKIP] people1_${num} 이미 REMEDIAL_BANK 존재`);
    continue;
  }

  // learning-common.js 태그 찾기
  const learningCommonPattern = /(<script\s+src=["'][^"']*learning-common\.js[^"']*["'][^>]*><\/script>)/;
  const match = content.match(learningCommonPattern);

  if (!match) {
    console.log(`[ERROR] people1_${num} learning-common.js 태그 없음`);
    continue;
  }

  const data = REMEDIAL_DATA[i].data;

  // REMEDIAL_BANK 스크립트를 learning-common.js 앞에 삽입
  const remedialScript = `<!-- ✅ 보완학습 데이터 (learning-common.js 로드 전에 정의) -->
<script>
window.REMEDIAL_BANK = ${JSON.stringify(data, null, 2)};
</script>

${match[0]}`;

  content = content.replace(learningCommonPattern, remedialScript);
  fs.writeFileSync(filePath, content, 'utf8');
  console.log(`[OK] people1_${num} REMEDIAL_BANK 삽입 완료 (${REMEDIAL_DATA[i].title})`);
}

