def replace_explain_block(content, unit, fixes):
    """유닛의 explain 블록 전체를 찾아서 q1~q4 값만 교체"""
    # 해당 유닛의 explain 블록 찾기
    pattern = rf"({unit}:[\s\S]*?explain:\s*\{{)([\s\S]*?)(\}}[\s,]*\n\s*detail:)"

    match = re.search(pattern, content)
    if not match:
        print(f"[{unit}] explain 블록을 찾을 수 없음")
        return content

    explain_block = match.group(2)

    # 각 q값 교체
    for qkey, new_val in fixes.items():
        # q1: '...' 또는 q1: "..." 패턴 찾기
        q_pattern = rf"({qkey}:\s*)['\"]([^'\"]*)['\"]"

        def replacer(m):
            return m.group(1) + "'" + new_val + "'"

        new_block = re.sub(q_pattern, replacer, explain_block, count=1)
        if new_block != explain_block:
            explain_block = new_block
            print(f"[{unit}] {qkey} 수정됨")
        else:
            print(f"[{unit}] {qkey} 패턴 불일치")

    # 전체 내용에 교체된 블록 반영
    new_content = content[:match.start(2)] + explain_block + content[match.end(2):]
    return new_content

for unit, fixes in explain_fixes.items():
    content = replace_explain_block(content, unit, fixes)

with open(filepath, 'w', encoding='utf-8') as f:
    f.write(content)

print("\n완료!")
