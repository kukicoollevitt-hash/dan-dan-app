for (let i = 1; i <= 10; i++) {
  const num = String(i).padStart(2, '0');
  const filePath = path.join(__dirname, 'public', 'BRAINUP', 'science', `physics_${num}.html`);

  if (!fs.existsSync(filePath)) {
    console.log(`[SKIP] ${filePath} 파일 없음`);
    continue;
  }

  let content = fs.readFileSync(filePath, 'utf8');

  // REMEDIAL_BANK 교체
  const bankData = REMEDIAL_BANKS[`physics_${num}`];
  if (!bankData) {
    console.log(`[SKIP] physics_${num} REMEDIAL_BANK 데이터 없음`);
    continue;
  }

  const newBankStr = `window.REMEDIAL_BANK = ${JSON.stringify(bankData, null, 2)};`;

  // 기존 REMEDIAL_BANK 찾아서 교체
  const bankRegex = /window\.REMEDIAL_BANK\s*=\s*\{[\s\S]*?\n\s*\};/;

  if (bankRegex.test(content)) {
    content = content.replace(bankRegex, newBankStr);
    console.log(`[OK] physics_${num} REMEDIAL_BANK 교체 완료`);
  } else {
    console.log(`[WARN] physics_${num} 기존 REMEDIAL_BANK 패턴 못찾음`);
  }

  fs.writeFileSync(filePath, content, 'utf8');
}

