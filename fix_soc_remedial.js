for (let i = 1; i <= 10; i++) {
  const num = String(i).padStart(2, '0');
  const key = `soc_${num}`;
  const filePath = path.join(__dirname, 'public', 'BRAINUP', 'social', `soc_${num}.html`);

  if (!fs.existsSync(filePath)) {
    console.log(`[SKIP] ${filePath} 파일 없음`);
    continue;
  }

  if (!REMEDIAL_DATA[key]) {
    console.log(`[SKIP] ${key} 데이터 없음`);
    continue;
  }

  let content = fs.readFileSync(filePath, 'utf8');

  // 기존 REMEDIAL_BANK 교체 또는 새로 추가
  const bankRegex = /const REMEDIAL_BANK\s*=\s*\{[\s\S]*?\n\s*\};/;

  if (bankRegex.test(content)) {
    content = content.replace(bankRegex, REMEDIAL_DATA[key].bank);
    console.log(`[OK] ${key} REMEDIAL_BANK 교체 완료`);
  } else {
    // <script> 태그 바로 뒤에 추가
    const scriptTagRegex = /(<script>)/;
    if (scriptTagRegex.test(content)) {
      content = content.replace(scriptTagRegex, `$1\n  ${REMEDIAL_DATA[key].bank}\n`);
      console.log(`[OK] ${key} REMEDIAL_BANK 추가 완료`);
    } else {
      console.log(`[FAIL] ${key} <script> 태그를 찾을 수 없음`);
      continue;
    }
  }

  fs.writeFileSync(filePath, content, 'utf8');
}

