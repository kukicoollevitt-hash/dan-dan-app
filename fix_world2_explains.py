with open(filepath, 'r', encoding='utf-8') as f:
    content = f.read()

for unit, fixes in explain_fixes.items():
    for qkey, new_text in fixes.items():
        # 현재 explain 값을 찾아서 수정
        # 패턴: q1: '기존값', 또는 q1: "기존값",
        pattern = rf"({unit}[\s\S]*?explain:\s*\{{\s*[\s\S]*?{qkey}:\s*)['\"]([^'\"]*)['\"]"

        def replacer(m):
            return m.group(1) + "'" + new_text + "'"

        new_content = re.sub(pattern, replacer, content, count=1)
        if new_content != content:
            content = new_content
            print(f"[{unit}] {qkey} 수정됨")
        else:
            print(f"[{unit}] {qkey} 패턴 불일치")

with open(filepath, 'w', encoding='utf-8') as f:
    f.write(content)

