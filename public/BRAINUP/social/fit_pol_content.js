/**
 * ✅ 단원 자동 인식 (강화)
 * 우선순위: ?unit=pol_XX → 파일명 pol_XX.html → 제목 숫자
 * ⚠️ HTML에서 이미 CUR_UNIT 설정했으면 덮어쓰지 않음
 */
(function () {
  // 이미 설정된 CUR_UNIT이 있고, pol_로 시작하면 덮어쓰지 않음
  if (window.CUR_UNIT && window.CUR_UNIT.startsWith('fit_pol_')) {
    console.log('[fit_pol_content.js] CUR_UNIT 이미 설정됨:', window.CUR_UNIT);
    return;
  }

  const qs = new URLSearchParams(location.search).get('unit');
  let unit = null;

  if (qs) {
    const m = qs.toLowerCase().match(/fit_pol[_-]?(d{1,2})/);
    if (m) unit = `fit_pol_${m[1].padStart(2, '0')}`;
  }

  if (!unit) {
    const m2 = location.pathname.toLowerCase().match(/fit_pol[_-]?(d{1,2})\.html/);
    if (m2) unit = `fit_pol_${m2[1].padStart(2, '0')}`;
  }

  if (!unit && document.title) {
    const m3 = document.title.match(/(\d{1,2})/);
    if (m3) unit = `fit_pol_${m3[1].padStart(2, '0')}`;
  }

  window.CUR_UNIT = unit || 'fit_pol_01';
  console.log('[fit_pol_content.js] CUR_UNIT 설정:', window.CUR_UNIT);
})();

