function getSeriesName(filename) {
  const match = filename.match(/^([a-z]+)/i);
  return match ? match[1].toLowerCase() : null;
}

function addVocabTips(content, vocabMap) {
  let modifiedContent = content;
  let addedCount = 0;

  // 한글 범위 정규식
  const koreanChar = /[가-힣]/;

  // 긴 단어 먼저 처리 (양분 → 양 순서로 처리되도록)
  const sortedWords = Object.entries(vocabMap)
    .sort((a, b) => b[0].length - a[0].length);

  for (const [word, meaning] of sortedWords) {
    // 1글자 단어는 오탐이 많으므로 건너뛰기
    if (word.length < 2) {
      continue;
    }

    // 이미 이 단어가 vocab-tip으로 감싸져 있으면 건너뛰기
    if (modifiedContent.includes(`">${word}</span>`)) {
      continue;
    }

    const escapedWord = word.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');

    // <p> 또는 <span> 태그 안의 텍스트에서 첫 번째 등장만 교체
    // 이미 태그 속성 안에 있는 건 건너뛰기
    const regex = new RegExp(`(>[^<]*)(${escapedWord})([^<]*<)`, 'g');
    let replaced = false;

    modifiedContent = modifiedContent.replace(regex, (match, before, foundWord, after) => {
      if (replaced) return match;

      // vocab-tip, data-meaning, class=, href= 등 속성 내부면 건너뛰기
      if (before.includes('vocab-tip') ||
          before.includes('data-meaning') ||
          before.includes('class=') ||
          before.includes('href=')) {
        return match;
      }

      // 단어 경계 체크: 앞이나 뒤에 한글이 있으면 건너뛰기
      // (두근거릴 → 근거 X, 자원봉사 → 자원 X)
      // 단, 뒤에 조사는 허용
      const charBefore = before.slice(-1);
      const charAfter = after.charAt(0);
      const isParticle = /^[을를이가은는에의로와과도만요다고며면서니나야라어아죠]$/.test(charAfter);

      if (koreanChar.test(charBefore)) {
        return match;
      }
      if (koreanChar.test(charAfter) && !isParticle) {
        return match;
      }

      replaced = true;
      addedCount++;
      return `${before}<span class="vocab-tip" data-meaning="${meaning}">${foundWord}</span>${after}`;
    });
  }

  return { content: modifiedContent, addedCount };
}

function processFile(filePath) {
  const filename = path.basename(filePath);
  const seriesName = getSeriesName(filename);

  let content = fs.readFileSync(filePath, 'utf8');

  // 시리즈별 어휘 + 공통 어휘 합치기
  const seriesVocab = SERIES_VOCAB[seriesName] || {};
  const combinedVocab = { ...seriesVocab, ...COMMON_VOCAB };

  // 어휘 툴팁 추가
  const result = addVocabTips(content, combinedVocab);

  if (result.addedCount > 0) {
    fs.writeFileSync(filePath, result.content, 'utf8');
    console.log(`✅ ${filename}: +${result.addedCount}개 추가`);
    return result.addedCount;
  } else {
    console.log(`⏭️  ${filename}: 추가할 어휘 없음`);
    return 0;
  }
}

function main() {
  // 시즌1 파일 목록 (_season1_34.html)
  const files = fs.readdirSync(CREATIVE_BOOK_DIR)
    .filter(f => f.endsWith('_season1_34.html'))
    .map(f => path.join(CREATIVE_BOOK_DIR, f));

  console.log(`\n📚 시즌1(3~4학년) 창작도서 vocab-tip 확장 시작\n`);
  console.log(`총 ${files.length}개 파일\n`);
  console.log(`공통 한자어/개념어: ${Object.keys(COMMON_VOCAB).length}개\n`);

  let totalAdded = 0;
  let processedFiles = 0;

  for (const file of files) {
    const added = processFile(file);
    if (added > 0) {
      totalAdded += added;
      processedFiles++;
    }
  }

  console.log(`\n📊 결과: ${processedFiles}개 파일에 총 ${totalAdded}개 어휘 추가\n`);
}

main();
