def create_q2_opts(answer_idx, paragraphs):
    """
    정답 위치(1-4)에 맞게 4개 선지 생성
    정답 선지만 실제 내용과 라벨이 일치
    """
    answer_idx = int(answer_idx) - 1  # 0-based index
    opts = []

    for i in range(4):
        if i == answer_idx:
            # 정답 선지: 라벨과 내용 일치
            opts.append(f'{i+1}문단 : {paragraphs[i]}')
        else:
            # 오답 선지: 다른 문단의 내용 사용 (라벨과 불일치)
            # 섞어서 배치
            other_indices = [j for j in range(4) if j != i and j != answer_idx]
            if len(other_indices) > 0:
                # 순환하도록 배치
                content_idx = other_indices[i % len(other_indices)]
                opts.append(f'{i+1}문단 : {paragraphs[content_idx]}')
            else:
                # fallback
                opts.append(f'{i+1}문단 : {paragraphs[(i+1)%4]}')

    return opts

def create_explanation(paragraphs):
    """해설 생성"""
    return (f'해설: 1문단의 중심 내용은 {paragraphs[0]}입니다. '
            f'2문단은 {paragraphs[1]}, '
            f'3문단은 {paragraphs[2]}, '
            f'4문단은 {paragraphs[3]}입니다.')

